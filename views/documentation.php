<section>
	<h5>Architecture</h5>
	<p>The setup of directories and files are setup for easy management.</p>
	<ul>
		<li><strong>app/</strong> - Here goes your classes or files for your application.</li>
		<ul>
			<li><strong>config/</strong> - Here goes configuration files.</li>
		</ul>
		<li><strong>assets/</strong> - Here goes CSS, Images, JavaScript, etc.</li>
		<li><strong>lib/</strong> - Here goes your libraries.</li>
		<li><strong>views/</strong> - For all the views that should be rendered.</li>
	</ul>
	<p>This simple site is already set up to show how you can use the architecture.</p>
</section>

